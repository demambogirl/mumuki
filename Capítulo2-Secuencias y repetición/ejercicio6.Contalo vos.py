def operacion_misteriosa(elementos):
  cantidad = 0

  for elemento in elementos:
    cantidad += 1

  return cantidad

# operacion_misteriosa es una función, porque devuelve algo
# operacion_misteriosa visita cada elemento de elementos, e incrementa cantidad en 1 por cada uno
# operacion_misteriosa inicializa una variable local cantidad en 0
# operacion_misteriosa hace lo mismo que la función len
# operacion_misteriosa no usa la variable elemento